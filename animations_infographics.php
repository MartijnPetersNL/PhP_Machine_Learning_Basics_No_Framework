    <h2>Infographics: What is a outlier in data?</h2>
    <p>An outlier is a data point that differs significantly from other observations. Outliers may indicate variability in the measurement or may be due to experimental errors; the latter are sometimes excluded from the data set.</p>
    <img src="images/datascience_outlier.png" alt="Infographic explaining outliers" class="infographic">
    <img src="images/clustering-outliers.png" alt="Graph explaining clustering outliers" class="infographic">
    <p>Outliers can skew and mislead the interpretation of results of statistical analyses.</p>
    <p> What are <strong>common causes of outliers?</strong></p>
    <ul>
        <li>Measurement error: inaccuracies in data collection can lead to outliers.</li>
        <li>Data entry error: mistakes made while inputting data can create outliers.</li>
        <li>Natural variability: some data points may be extreme but valid observations.</li>
    </ul>
    <p> What are <strong>uncommon causes of outliers?</strong></p>
    <ul>
        <li>Novelty: new and unique events may not fit existing patterns.</li>
        <li>Contextual factors: changes in the environment can affect data collection.</li>
        <li>Intentional manipulation: data may be altered to achieve a desired outcome, such as manually adding data points.</li>
    </ul>
    <p> Note: Intentional manipulation of data is unethical and can lead to significant consequences in research and business.</p>
    <p> Specific intentional manipulation can lead to jurisdictional issues and legal consequences.</p>
