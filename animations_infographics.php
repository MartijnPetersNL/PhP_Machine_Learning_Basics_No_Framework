    <h2> Common AI tool misconceptions</h2>
    <p> Myth: ai tools came out of nowhere and are its own seperate program type </p>
    <p> Reality: AI tools are built on existing technologies and frameworks, and they require careful integration into existing systems.</p>
    <p> AI tools are a specialization in the field of machine learning, and they leverage advanced algorithms and data processing techniques to deliver insights and predictions.</p>
    <p> AI tools give good output when the input, the prompts set a clear context and expectations.</p>
    <p> Ask yourself as a AI tool user: can i summarize what i really want as output in a few sentences, wihtout sounding vague?</p>
    <p> AI tools are definetly not magic wands that can solve all problems without human intervention.</p>
    <br/>
    <p> Unlike humans, AI can not replicate the full range of human cognitive abilities, such as emotional intelligence, creativity, and ethical reasoning.</p>
    <p> A fair equivalant of machine labour is Robot arms in car factories, which can perform repetitive tasks with high precision and speed.</p>

    <p> Neither AI tools nor Robot arms can fully replace human workers, as they lack the ability to understand context, make complex decisions, and exhibit empathy.</p>
    <p> Therefore, AI will not replace human workers, but rather augment their capabilities and help them work more efficiently.</p>
    <p> In the short term that will mean the repetitive tasks will be automated, but in the long term it will mean that AI will be used to assist humans in their work even more.</p>
    <br/>
